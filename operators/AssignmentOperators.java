public class AssignmentOperators {
    public static void main(String[] args) {
        int a = 10;

        a += 5; 
        System.out.println("After += 5, a = " + a);
        a -= 3;  
        System.out.println("After -= 3, a = " + a);
        a *= 2; 
        System.out.println("After *= 2, a = " + a);
        a /= 4; 
        System.out.println("After /= 4, a = " + a);
        a %= 5;  
        System.out.println("After %= 5, a = " + a);
    }
}
